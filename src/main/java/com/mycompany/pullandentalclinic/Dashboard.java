private boolean isAdmin(String username) {
    boolean isAdmin = false;
    String query = "SELECT usersaccess FROM users WHERE usersusername = ?";
    
    try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/pullandentalclinic", "root", "root");
         PreparedStatement pstmt = conn.prepareStatement(query)) {
         
        pstmt.setString(1, username);
        ResultSet rs = pstmt.executeQuery();
        
        if (rs.next()) {
            String accessLevel = rs.getString("usersaccess");
            isAdmin = "Admin".equalsIgnoreCase(accessLevel);
        }
    } catch (SQLException e) {
        e.printStackTrace();
    }
    
    return isAdmin;
}
