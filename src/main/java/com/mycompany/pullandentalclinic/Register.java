    private void regcityzipcodeKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_regcityzipcodeKeyTyped
                              
    char c = evt.getKeyChar();
    String currentText = regcityzipcode.getText();

    // Allow backspace and delete keys
    if (c == KeyEvent.VK_BACK_SPACE || c == KeyEvent.VK_DELETE) {
        return;
    }

    // Check if the character is a digit and the length is less than 4
    if (!Character.isDigit(c) || currentText.length() >= 4) {
        evt.consume(); // Ignore the key event
    }

    }//GEN-LAST:event_regcityzipcodeKeyTyped

